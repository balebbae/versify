import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const raw = fs.readFileSync(path.join(__dirname, "chapters-raw.txt"), "utf8");

const SLUGS = {
  "Philippians 1": "philippians-1",
  "Philippians 2": "philippians-2",
  "Philippians 3": "philippians-3",
  "Philippians 4": "philippians-4",
  "Colossians 1": "colossians-1",
  "Colossians 2": "colossians-2",
  "Colossians 3": "colossians-3",
  "Colossians 4": "colossians-4",
};

const titleRe = /^(Philippians|Colossians) \d+$/;

// Split raw text into chapter blocks keyed by their title line.
const lines = raw.split("\n");
const blocks = [];
let current = null;
for (const line of lines) {
  if (titleRe.test(line.trim())) {
    current = { title: line.trim(), bodyLines: [] };
    blocks.push(current);
  } else if (current) {
    current.bodyLines.push(line);
  }
}

// Lines that begin with a digit are verse-content lines; everything else
// (section headings) is dropped.
function isVerseLine(line) {
  return /^\s*\d/.test(line);
}

// Stopwords that are never treated as key words.
const STOP = new Set(
  `a an the and or but if so as of to in on at by for with from into within
   is are was were be been being am do does did done have has had having
   not no nor than then that this these those there here it its they them their
   he him his she her we us our you your i me my mine myself himself themselves
   yourselves yourself one another who whom whose which what when where how why
   will would shall should can could may might must also even just only ever
   always now more most much many all any some such other others both each every
   about above under over out up off down upngot per about because since while
   though although however therefore indeed yet still about toward against
   between among through according o`.split(/\s+/)
);

// Words that are always key when present (divine names / weighty terms).
const ALWAYS = new Set(
  `god christ jesus lord spirit father son holy gospel faith grace peace love
   hope glory salvation cross death life resurrection sin sins righteousness
   mystery church kingdom`.split(/\s+/)
);

function strip(w) {
  return w.replace(/[^a-zA-Z0-9']/g, "");
}

function pickKeywords(text) {
  const words = text.split(/\s+/).filter(Boolean);
  const scored = [];
  const seen = new Set();
  words.forEach((w) => {
    const s = strip(w);
    const n = s.toLowerCase();
    if (!n || n.length < 3) return;
    if (STOP.has(n)) return;
    let score = 0;
    if (ALWAYS.has(n)) score += 5;
    if (/^[A-Z]/.test(s)) score += 2; // proper-ish noun / emphasis
    if (s.length >= 7) score += 2;
    else if (s.length >= 5) score += 1;
    scored.push({ word: s, n, score, hasAlpha: /[a-zA-Z]/.test(s) });
  });
  // Number of blanks: a light touch — roughly a quarter of meaningful words,
  // capped so a verse keeps most of its text visible.
  const target = Math.max(1, Math.min(6, Math.round(scored.length * 0.28)));
  scored.sort((a, b) => b.score - a.score);
  const keywords = [];
  for (const c of scored) {
    if (keywords.length >= target) break;
    if (seen.has(c.n)) continue;
    seen.add(c.n);
    keywords.push(c.word);
  }
  return keywords;
}

const out = [];
for (const block of blocks) {
  const slug = SLUGS[block.title];
  const [book, chapter] = block.title.split(" ");
  // Join only verse-content lines, dropping section headings.
  const body = block.bodyLines
    .filter(isVerseLine)
    .join(" ")
    .replace(/\s+/g, " ")
    .trim();
  // Insert a delimiter before each verse number. Verse numbers are the only
  // digits in the text and always sit at the start or after whitespace; the
  // following char may be a letter, quote, or punctuation like "(".
  const marked = body.replace(/(^|\s)(\d+)\s*/g, "\n$2 ");
  const parts = marked.split("\n").filter((p) => p.trim());
  const verses = parts.map((p) => {
    const m = p.match(/^(\d+)\s+([\s\S]*)$/);
    const number = parseInt(m[1], 10);
    const text = m[2].trim();
    return { number, text, keywords: pickKeywords(text) };
  });
  out.push({ slug, book, chapter: parseInt(chapter, 10), reference: block.title, verses });
}

// Emit TypeScript.
function esc(s) {
  return s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}
let ts = `export interface ChapterVerse {
  number: number;
  text: string;
  keywords: string[];
}

export interface Chapter {
  slug: string;
  book: string;
  chapter: number;
  reference: string;
  verses: ChapterVerse[];
}

// NASB text supplied by the user. Single round of fill-in-the-blank: only the
// listed key words are blanked. Generated by scripts/build-chapters.mjs.
export const chapters: Chapter[] = [
`;
for (const c of out) {
  ts += `  {\n    slug: "${c.slug}",\n    book: "${c.book}",\n    chapter: ${c.chapter},\n    reference: "${c.reference}",\n    verses: [\n`;
  for (const v of c.verses) {
    ts += `      {\n        number: ${v.number},\n        text: "${esc(v.text)}",\n        keywords: [${v.keywords
      .map((k) => `"${esc(k)}"`)
      .join(", ")}],\n      },\n`;
  }
  ts += `    ],\n  },\n`;
}
ts += `];

export function getChapter(slug: string): Chapter | undefined {
  return chapters.find((c) => c.slug === slug);
}
`;

fs.writeFileSync(path.join(__dirname, "..", "lib", "chapters.ts"), ts);

// Print a quick summary for review.
for (const c of out) {
  console.log(`\n=== ${c.reference} (${c.verses.length} verses) ===`);
  for (const v of c.verses.slice(0, 3)) {
    console.log(`${v.number}: keywords -> ${v.keywords.join(", ")}`);
  }
}
console.log(
  "\nTotal verses:",
  out.reduce((a, c) => a + c.verses.length, 0)
);
